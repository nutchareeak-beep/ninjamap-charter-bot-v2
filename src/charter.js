export function buildCharterIntro(version) {
  return [
    "# Ninjamap Community Charter 2026",
    "",
    "Acceptance mode: single-button",
    "",
    "ยินดีต้อนรับสู่ Ninjamap Community",
    "ก่อนที่ท่านจะสามารถเข้าถึงห้องต่างๆ ภายใน Community ได้ กรุณาอ่านและยืนยันข้อตกลงต่อไปนี้ให้ครบทุกข้อ",
    "",
    `Charter version: ${version}`
  ].join("\n");
}

export function buildSectionBody(section, index, total) {
  const items = section.items.map((item) => `☐ ${item}`).join("\n\n");
  return [`## ${section.title}`, "", items, "", `ขั้นตอนที่ ${index + 1}/${total}: ${section.label}`].join("\n");
}

